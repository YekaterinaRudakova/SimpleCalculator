package com.it_academy.calculator;

public class ShowerResult {
    public static void showAdditionResult(double result) {
        System.out.printf("The result of addition is %.5f%n", result);
    }

    public static void showSubtractionResult(double result) {
        System.out.printf("The result of subtraction is  %.5f%n", result);
    }

    public static void showMultiplicationResult(double result) {
        System.out.printf("The result of multiplication is %.5f%n", result);
    }

    public static void showDivisionResult(double result) {
        System.out.printf("The result of division is  %.5f%n", result);
    }
}
